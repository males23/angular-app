/** Ce composant cree pour service de parent a l'integration
* de notre formullaire fils app-service-form */
import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { Service } from '../service';
import { ServiceService } from '../service.service';

@Component({
  selector: 'app-edit-service',
  templateUrl: './edit-service.component.html',
  styleUrls: ['./edit-service.component.css']
})
export class EditServiceComponent implements OnInit {

  service: Service | undefined;

  //On recupere l'id de la route et le service
  constructor(
    private route: ActivatedRoute,
    private serviceService: ServiceService
  ) { }

  /** On recupere le serviceId ou l'id du service qui vient de l'URL
   * Je vais chercher le service associe 
   * et s'il n'y a pas, je mets undefined */
    ngOnInit() {
      const serviceId: string|null = this.route.snapshot.paramMap.get('id');
      if(serviceId) {
        //On recuperait les donnees d'une maniere synchrone
        //this.service = this.serviceService.getServiceById(+serviceId);

 //Maintenat on va recuperaer  les donnees d'une maniere asynchrone
        this.serviceService.getServiceById(+serviceId)
        .subscribe(service => this.service = service);
      } else {
        this.service = undefined;
      }
    }
}












































































































































































































































































































































































































































































